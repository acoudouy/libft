#include "libft.h"

void		*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*s2;

	i = 0;
	s2 = (unsigned char *)s;
	while (i < n && s2[i] != (unsigned char)c)
		i++;
	if (i != n)
		return ((void *)(s + i));
	else
		return (NULL);
}
